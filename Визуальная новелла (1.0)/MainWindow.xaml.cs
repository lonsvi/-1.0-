        //Настраивает кнопки выборов и их обработчики
        //Загружает соответствующую сцену при выборе

        private void HandleChoices(Dictionary<string, int> choices) //Метод обработки выборов
        {
            if (choices == null || choices.Count == 0) // Устанавливает видимость ChoicePanel в зависимости от наличия выборов
            {
                ChoicePanel.Visibility = Visibility.Collapsed;
                return;
            }

            ChoicePanel.Visibility = Visibility.Visible;
            var choicesList = choices.ToList();

            // Очищаем предыдущие обработчики
            ChoiceButton1.Click -= ChoiceButton1_Click;
            ChoiceButton2.Click -= ChoiceButton2_Click;

            if (choicesList.Count >= 1) // Настраивает первый выбор и добавляет обработчик события клика
            {
                ChoiceButton1.Content = choicesList[0].Key;
                ChoiceButton1.Click += ChoiceButton1_Click;
            }

            if (choicesList.Count >= 2) // Настраивает второй выбор и добавляет обработчик события клика
           
            {
                ChoiceButton2.Content = choicesList[1].Key;
                ChoiceButton2.Click += ChoiceButton2_Click;
            }

            void ChoiceButton1_Click(object sender, RoutedEventArgs e)  // Обработчик события клика для первого выбора
            {
                LoadScene(choicesList[0].Value);
            }

            void ChoiceButton2_Click(object sender, RoutedEventArgs e) // Обработчик события клика для второго выбора
            {
                LoadScene(choicesList[1].Value);
            }
        }
