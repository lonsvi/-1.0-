        private void NextScene() //Метод перехода к следующей сцене
        {
            if (textTimer.IsEnabled) //Если текст ещё анимируется - показывает весь текст сразу
            {
                textTimer.Stop();
                DialogText.Text = fullText;
                return;
            }

            if (currentScene + 1 < scenes.Count) //Иначе переходит к следующей сцене
            {
                LoadScene(currentScene + 1);
            }
        }



