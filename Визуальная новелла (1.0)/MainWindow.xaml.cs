        private void TextTimer_Tick(object sender, EventArgs e) //Метод для анимации текста
        {
            // Метод управляет отображением текста по одному символу с таймером
            if (currentCharIndex < fullText.Length)
            {
                
                DialogText.Text += fullText[currentCharIndex]; // Добавляет текущий символ из fullText к DialogText
