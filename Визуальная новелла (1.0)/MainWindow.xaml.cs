        private void LoadScene(int index) //Метод загрузки сцены
        {
            //Загружает фон и спрайт персонажа
            //Устанавливает имя персонажа и текст диалога
            //выборы, если они есть
            //Запускает анимацию текста

            if (index >= 0 && index < scenes.Count) // Проверяем, что индекс сцены в диапазоне
            {
                var scene = scenes[index]; // Получаем сцену по индексу
                currentScene = index; // Устанавливаем текущую сцену

                SceneNumberDisplay.Text = $"Сцена: {index}"; //дебаг - отображает номер текущей сцены

              

                // Обработка выборов
                if (scene.Choices != null && scene.Choices.Any()) // Если в сцене есть выборы
                {
                    HandleChoices(scene.Choices); // Обрабатываем выборы
                }
                else
                {
                    ChoicePanel.Visibility = Visibility.Collapsed; // Скрываем панель выборов, если в сцене нет выборов
                }

                // Загрузка фона и спрайта
                if (!string.IsNullOrEmpty(scene.BackgroundPath))
                {// Формирует полный путь к файлу фона, объединяя базовый каталог приложения с относительным путем

                    string backgroundPath = System.IO.Path.Combine(AppDomain.CurrentDomain.BaseDirectory, "BackGround", scene.BackgroundPath);
                    // Устанавливает источник изображения фона, используя созданный Uri из полного пути к файлу
                    BackgroundImage.Source = new BitmapImage(new Uri(backgroundPath));
                }

                if (!string.IsNullOrEmpty(scene.CharacterPath))
                {
                    string characterPath = string.Empty; // Значение по умолчанию

                    if (scene.CharacterPath.Contains("noFON"))
                    {
                        characterPath = System.IO.Path.Combine(
                            AppDomain.CurrentDomain.BaseDirectory,
                            "Sprite Pers",
                            scene.CharacterPath
                        );
                    }
                    else if (scene.CharacterPath.Contains("Rin"))
                    {
                        if (scene.CharacterPath.Contains("Summer"))
                        {
                            characterPath = System.IO.Path.Combine(
                                AppDomain.CurrentDomain.BaseDirectory,
                                "Sprite Pers",
                                "Rina",
                                "Summer_Uniform",
                                scene.CharacterPath
                            );
                        }
                        else if (scene.CharacterPath.Contains("Winter"))
                        {
                            characterPath = System.IO.Path.Combine(
                                AppDomain.CurrentDomain.BaseDirectory,
                                "Sprite Pers",
                                "Rina",
                                "Winter_Uniform",
                                scene.CharacterPath
